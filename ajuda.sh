#!/bin/bash

arg () {
####################################################
	while (( "$#" )); do        # RODA O LOOP A QUANTIDADE DE ARGUMENTOS
	echo "$# -- $1"
		  case "$1" in
			  -e|-E)
				echo "editar"
				gedit $0 && exit
				  exit 0
				  ;;
			  -a|-A)
				  echo "
#############################################################################
#############################################################################
				AJUDA
#############################################################################
#############################################################################
Este é um texto de teste que deve ser subtituido!
"
				  exit 0
				  ;;
			  -t)
				  echo "teste"
				  exit 0
				  ;;
			    *)
				  echo "nenhuma opção"
				  exit 0
		  esac
	shift
	done
####################################################
}
arg $@
